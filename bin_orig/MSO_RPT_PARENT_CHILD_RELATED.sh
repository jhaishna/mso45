#!/bin/sh
#source .bash_profile
user="$1"
pass="$2"
SID="$3"
location="$4"
#rm $location/execution_logs
sqlplus -S $user/$pass@$SID <<EOF
set termout off
set feedback off
set echo off
set linesize 50
set pagesize 30000
SET MARKUP HTML ON SPOOL ON PREFORMAT OFF ENTMAP ON
spool $location/MSO_RPT_PARENT_CHILD_RELATED.xls
EXECUTE MSO_PARENT_CHILD_BOX_RELATED;
select SALES_HEAD_P,SALES_MANAGER_P,LCO_CODE_P,LCO_NAME,LCO_LOCATION,DT_CODE_P,DT_NAME,LCO_ACT_SER_COUNT,LCO_INACT_SER_COUNT,LCO_COUNT_SER_CRFY,LCO_COUNT_STB_GIVEN,LC0_PARENT_CONNECTION,LC0_PARENT_BILLING_AMT,LC0_CHILD_C1_CONNECTION,LC0_CHILD_C1_BILLING_AMT,LC0_CHILD_C2_CONNECTION,LC0_CHILD_C2_BILLING_AMT from MSO_RPT_BRS_PARENT_CHILD_T;
exit;
spool off;
EOF
